// =============================================================================
// Конструктор класса AES
// =============================================================================
AES::AES(AESKeyLength keyLength) 
    : keyLength_(keyLength)  // Инициализируем поле класса
{
    // Валидация входного параметра
    if (keyLength_ != AES_128 && keyLength_ != AES_192 && keyLength_ != AES_256) {
        keyLength_ = AES_256;  // Значение по умолчанию для безопасности
    }
}

// =============================================================================
// Вспомогательная функция для получения Nk (количество 32-битных слов в ключе)
// =============================================================================
uint32_t AES::getNk() const {
    switch(keyLength_) {
        case AES_128: return 4;  // 128 бит = 4 слова по 32 бита
        case AES_192: return 6;  // 192 бита = 6 слов
        case AES_256: return 8;  // 256 бит = 8 слов
        default: return 4;        // Безопасное значение по умолчанию
    }
}

// =============================================================================
// Вспомогательная функция для получения Nr (количество раундов)
// =============================================================================
uint32_t AES::getNr() const {
    return getNk() + 6;  // Nk + 6 = 10, 12 или 14 раундов
}

// =============================================================================
// Расширение ключа (Key Expansion) — ИСПРАВЛЕНО с правильным вычислением Nk
// =============================================================================
void AES::keyExpansion(const uint8_t* key, uint8_t* roundKey) {
    // КРИТИЧЕСКАЯ ПРОВЕРКА: защита от нулевых указателей
    if (!key || !roundKey) {
        return;
    }
    
    // Получаем правильные значения Nk и Nr из keyLength_
    const uint32_t Nk = getNk();  // Теперь Nk будет 4, 6 или 8, а не 0!
    const uint32_t Nr = getNr();
    
    // Проверка корректности значений
    if (Nk != 4 && Nk != 6 && Nk != 8) {
        return;  // Некорректная длина ключа
    }
    
    uint32_t i, j, k;
    uint8_t tempa[4];

    // Копируем исходный ключ в начало roundKey
    for (i = 0; i < Nk; ++i) {
        // Проверка границ для чтения key
        // key должен содержать Nk*4 байт
        roundKey[i * 4 + 0] = key[i * 4 + 0];
        roundKey[i * 4 + 1] = key[i * 4 + 1];
        roundKey[i * 4 + 2] = key[i * 4 + 2];
        roundKey[i * 4 + 3] = key[i * 4 + 3];
    }

    // Генерируем остальные слова (всего нужно (Nr+1)*16 байт = (Nr+1)*4 слов)
    for (i = Nk; i < 4 * (Nr + 1); ++i) {
        k = (i - 1) * 4;
        
        // Проверка границ: убеждаемся, что не выходим за пределы roundKey
        if (k + 3 >= 240) {  // roundKey имеет размер 240 байт
            return;
        }
        
        tempa[0] = roundKey[k + 0];
        tempa[1] = roundKey[k + 1];
        tempa[2] = roundKey[k + 2];
        tempa[3] = roundKey[k + 3];

        // Каждые Nk слов выполняем специальную обработку
        if (i % Nk == 0) {
            // RotWord: циклический сдвиг влево на 1 байт
            uint8_t t = tempa[0];
            tempa[0] = sbox[tempa[1]];
            tempa[1] = sbox[tempa[2]];
            tempa[2] = sbox[tempa[3]];
            tempa[3] = sbox[t];
            // XOR с раундовой константой
            uint32_t rcon_index = i / Nk - 1;
            if (rcon_index < 10) {  // Защита от выхода за границы rcon
                tempa[0] ^= rcon[rcon_index];
            }
        } 
        // Для AES-256 дополнительная обработка на середине
        else if (Nk > 6 && i % Nk == 4) {
            tempa[0] = sbox[tempa[0]];
            tempa[1] = sbox[tempa[1]];
            tempa[2] = sbox[tempa[2]];
            tempa[3] = sbox[tempa[3]];
        }

        j = i * 4;
        k = (i - Nk) * 4;
        
        // Проверка границ перед записью
        if (j + 3 >= 240 || k + 3 >= 240) {
            return;
        }
        
        roundKey[j + 0] = roundKey[k + 0] ^ tempa[0];
        roundKey[j + 1] = roundKey[k + 1] ^ tempa[1];
        roundKey[j + 2] = roundKey[k + 2] ^ tempa[2];
        roundKey[j + 3] = roundKey[k + 3] ^ tempa[3];
    }
}

// =============================================================================
// Добавление раундового ключа (AddRoundKey)
// =============================================================================
void AES::addRoundKey(uint8_t state[4][4], const uint8_t* roundKey) {
    if (!state || !roundKey) return;
    
    for (int i = 0; i < 4; ++i) {
        for (int j = 0; j < 4; ++j) {
            state[j][i] ^= roundKey[i * 4 + j];
        }
    }
}

// =============================================================================
// Замена байтов через S-Box (SubBytes)
// =============================================================================
void AES::subBytes(uint8_t state[4][4]) {
    if (!state) return;
    
    for (int i = 0; i < 4; ++i) {
        for (int j = 0; j < 4; ++j) {
            state[i][j] = sbox[state[i][j]];
        }
    }
}

// =============================================================================
// Сдвиг строк (ShiftRows)
// =============================================================================
void AES::shiftRows(uint8_t state[4][4]) {
    if (!state) return;
    
    uint8_t temp;
    
    // Строка 1: сдвиг на 1 влево
    temp = state[1][0];
    state[1][0] = state[1][1];
    state[1][1] = state[1][2];
    state[1][2] = state[1][3];
    state[1][3] = temp;
    
    // Строка 2: сдвиг на 2 влево
    temp = state[2][0];
    state[2][0] = state[2][2];
    state[2][2] = temp;
    temp = state[2][1];
    state[2][1] = state[2][3];
    state[2][3] = temp;
    
    // Строка 3: сдвиг на 3 влево (эквивалентно сдвигу на 1 вправо)
    temp = state[3][3];
    state[3][3] = state[3][2];
    state[3][2] = state[3][1];
    state[3][1] = state[3][0];
    state[3][0] = temp;
}

// =============================================================================
// Перемешивание столбцов (MixColumns)
// =============================================================================
void AES::mixColumns(uint8_t state[4][4]) {
    if (!state) return;
    
    for (int i = 0; i < 4; ++i) {
        uint8_t a[4];
        uint8_t b[4];
        
        for (int j = 0; j < 4; ++j) {
            a[j] = state[j][i];
            // Умножение на 2 в GF(2^8)
            b[j] = (state[j][i] & 0x80) ? ((state[j][i] << 1) ^ 0x1B) : (state[j][i] << 1);
        }
        
        state[0][i] = b[0] ^ a[1] ^ b[1] ^ a[2] ^ a[3];
        state[1][i] = a[0] ^ b[1] ^ a[2] ^ b[2] ^ a[3];
        state[2][i] = a[0] ^ a[1] ^ b[2] ^ a[3] ^ b[3];
        state[3][i] = a[0] ^ b[0] ^ a[1] ^ a[2] ^ b[3];
    }
}

// =============================================================================
// Обратная замена байтов (InvSubBytes)
// =============================================================================
void AES::invSubBytes(uint8_t state[4][4]) {
    if (!state) return;
    
    for (int i = 0; i < 4; ++i) {
        for (int j = 0; j < 4; ++j) {
            state[i][j] = rsbox[state[i][j]];
        }
    }
}

// =============================================================================
// Обратный сдвиг строк (InvShiftRows)
// =============================================================================
void AES::invShiftRows(uint8_t state[4][4]) {
    if (!state) return;
    
    uint8_t temp;
    
    // Строка 1: сдвиг на 1 вправо
    temp = state[1][3];
    state[1][3] = state[1][2];
    state[1][2] = state[1][1];
    state[1][1] = state[1][0];
    state[1][0] = temp;
    
    // Строка 2: сдвиг на 2 вправо
    temp = state[2][0];
    state[2][0] = state[2][2];
    state[2][2] = temp;
    temp = state[2][1];
    state[2][1] = state[2][3];
    state[2][3] = temp;
    
    // Строка 3: сдвиг на 3 вправо
    temp = state[3][0];
    state[3][0] = state[3][1];
    state[3][1] = state[3][2];
    state[3][2] = state[3][3];
    state[3][3] = temp;
}

// =============================================================================
// Обратное перемешивание столбцов (InvMixColumns)
// =============================================================================
void AES::invMixColumns(uint8_t state[4][4]) {
    if (!state) return;
    
    for (int i = 0; i < 4; ++i) {
        uint8_t a[4];
        
        for (int j = 0; j < 4; ++j) {
            a[j] = state[j][i];
        }
        
        state[0][i] = 
            mul14[a[0]] ^ mul11[a[1]] ^ mul13[a[2]] ^ mul9[a[3]];
        state[1][i] = 
            mul9[a[0]] ^ mul14[a[1]] ^ mul11[a[2]] ^ mul13[a[3]];
        state[2][i] = 
            mul13[a[0]] ^ mul9[a[1]] ^ mul14[a[2]] ^ mul11[a[3]];
        state[3][i] = 
            mul11[a[0]] ^ mul13[a[1]] ^ mul9[a[2]] ^ mul14[a[3]];
    }
}

// =============================================================================
// Шифрование в режиме CBC (Cipher Block Chaining)
// =============================================================================
std::vector<uint8_t> AES::EncryptCBC(const uint8_t* input, size_t length, 
                                      const uint8_t* key, const uint8_t* iv) {
    // Проверка входных параметров
    if (!input || !key || !iv) {
        return std::vector<uint8_t>();
    }
    
    if (length == 0) {
        return std::vector<uint8_t>();
    }
    
    // Проверка на переполнение при вычислении padded_len
    const size_t block_size = 16;
    if (length > (std::numeric_limits<size_t>::max() - block_size)) {
        return std::vector<uint8_t>();
    }
    
    size_t padded_len = length + (block_size - (length % block_size));
    if (padded_len < length) {  // Проверка переполнения
        return std::vector<uint8_t>();
    }
    
    // Инициализация раундовых ключей
    uint8_t roundKey[240] = {0};
    keyExpansion(key, roundKey);
    
    // Подготовка данных с PKCS#7 padding
    std::vector<uint8_t> padded;
    try {
        padded.assign(input, input + length);
        uint8_t pad_value = static_cast<uint8_t>(padded_len - length);
        padded.resize(padded_len, pad_value);
    } catch (const std::bad_alloc&) {
        return std::vector<uint8_t>();
    } catch (const std::exception&) {
        return std::vector<uint8_t>();
    }
    
    // Выделение памяти для выходных данных
    std::vector<uint8_t> output;
    try {
        output.resize(padded_len);
    } catch (const std::bad_alloc&) {
        return std::vector<uint8_t>();
    }
    
    // Буфер для предыдущего блока (начинаем с IV)
    uint8_t prev[16];
    std::memcpy(prev, iv, 16);
    
    // Шифрование по блокам
    const uint8_t num_rounds = getNr();  // Используем правильное количество раундов
    
    for (size_t i = 0; i < padded_len; i += block_size) {
        // Проверка границ
        if (i + block_size > padded.size()) {
            return std::vector<uint8_t>();
        }
        
        // XOR с предыдущим блоком
        for (size_t j = 0; j < block_size; ++j) {
            padded[i + j] ^= prev[j];
        }
        
        // Загрузка состояния
        uint8_t state[4][4];
        std::memcpy(state, padded.data() + i, block_size);
        
        // Начальный раунд
        addRoundKey(state, roundKey);
        
        // Основные раунды
        for (uint8_t round = 1; round < num_rounds; ++round) {
            subBytes(state);
            shiftRows(state);
            mixColumns(state);
            addRoundKey(state, roundKey + round * 16);
        }
        
        // Финальный раунд
        subBytes(state);
        shiftRows(state);
        addRoundKey(state, roundKey + (num_rounds * 16));
        
        // Сохранение результата
        std::memcpy(output.data() + i, state, block_size);
        std::memcpy(prev, state, block_size);
    }
    
    return output;
}

// =============================================================================
// Дешифрование в режиме CBC
// =============================================================================
std::vector<uint8_t> AES::DecryptCBC(const uint8_t* input, size_t length, 
                                      const uint8_t* key, const uint8_t* iv) {
    // Проверка входных параметров
    if (!input || !key || !iv) {
        return std::vector<uint8_t>();
    }
    
    if (length == 0 || length % 16 != 0) {
        return std::vector<uint8_t>();
    }
    
    // Инициализация раундовых ключей
    uint8_t roundKey[240] = {0};
    keyExpansion(key, roundKey);
    
    // Выделение памяти для выходных данных
    std::vector<uint8_t> output;
    try {
        output.resize(length);
    } catch (const std::bad_alloc&) {
        return std::vector<uint8_t>();
    }
    
    // Буфер для предыдущего блока
    uint8_t prev[16];
    std::memcpy(prev, iv, 16);
    
    // Дешифрование по блокам
    const size_t block_size = 16;
    const uint8_t num_rounds = getNr();  // Используем правильное количество раундов
    
    for (size_t i = 0; i < length; i += block_size) {
        // Загрузка состояния
        uint8_t state[4][4];
        std::memcpy(state, input + i, block_size);
        
        // Финальный раунд (начало дешифрования)
        addRoundKey(state, roundKey + (num_rounds * 16));
        
        // Основные раунды в обратном порядке
        for (uint8_t round = num_rounds - 1; round > 0; --round) {
            invShiftRows(state);
            invSubBytes(state);
            addRoundKey(state, roundKey + round * 16);
            invMixColumns(state);
        }
        
        // Начальный раунд (конец дешифрования)
        invShiftRows(state);
        invSubBytes(state);
        addRoundKey(state, roundKey);
        
        // XOR с предыдущим зашифрованным блоком
        for (size_t j = 0; j < block_size; ++j) {
            reinterpret_cast<uint8_t*>(state)[j] ^= prev[j];
        }
        
        // Сохранение результата
        std::memcpy(output.data() + i, state, block_size);
        std::memcpy(prev, input + i, block_size);
    }
    
    // Удаление PKCS#7 padding
    if (!output.empty()) {
        uint8_t pad = output.back();
        if (pad > 0 && pad <= block_size) {
            bool valid_padding = true;
            for (uint8_t k = 1; k <= pad; ++k) {
                if (output[output.size() - k] != pad) {
                    valid_padding = false;
                    break;
                }
            }
            if (valid_padding) {
                output.resize(output.size() - pad);
            }
        }
    }
    
    return output;
}
